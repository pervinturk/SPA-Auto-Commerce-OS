def load():
    global _current
    saved = db.get_setting("lang", "tr")
    _current = saved if saved in _T else "tr"


def get_lang() -> str:
    return _current


def set_lang(code: str):
    global _current
    if code not in _T:
        return
    _current = code
    db.set_setting("lang", code)
    for cb in _observers:
        try:
            cb()
        except Exception:
            pass


def subscribe(cb):
    _observers.append(cb)


def t(key: str) -> str:
    return _T.get(_current, _T["tr"]).get(key, _T["tr"].get(key, key))
